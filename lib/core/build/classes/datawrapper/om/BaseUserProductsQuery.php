/**
 * Base class that represents a query for the 'user_products' table.
 *
 *
 *
 * @method UserProductsQuery orderByUserId($order = Criteria::ASC) Order by the user_id column
 * @method UserProductsQuery orderByProductId($order = Criteria::ASC) Order by the product_id column
 *
 * @method UserProductsQuery groupByUserId() Group by the user_id column
 * @method UserProductsQuery groupByProductId() Group by the product_id column
